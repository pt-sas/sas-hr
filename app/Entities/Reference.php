<?php

namespace App\Entities;

use CodeIgniter\Entity\Entity;

class Reference extends Entity
{
    protected $sys_reference_id;
    protected $name;
    protected $description;
    protected $validationtype;
    protected $isactive;
    protected $created_by;
    protected $updated_by;

    protected $dates   = [
        'created_at',
        'updated_at',
        'deleted_at',
    ];

    public function getReferenceId()
    {
        return $this->attributes['sys_reference_id'];
    }

    public function setReferenceId($sys_reference_id)
    {
        $this->attributes['sys_reference_id'] = $sys_reference_id;
    }

    public function getName()
    {
        return $this->attributes['name'];
    }

    public function setName($name)
    {
        $this->attributes['name'] = $name;
    }

    public function getDescription()
    {
        return $this->attributes['description'];
    }

    public function setDescription($description)
    {
        $this->attributes['description'] = $description;
    }

    public function getValidationType()
    {
        return $this->attributes['validationtype'];
    }

    public function setValidationType($validationtype)
    {
        $this->attributes['validationtype'] = $validationtype;
    }

    public function getIsActive()
    {
        return $this->attributes['isactive'];
    }

    public function setIsActive($isactive)
    {
        return $this->attributes['isactive'] = $isactive;
    }

    public function getCreatedAt()
    {
        return $this->attributes['created_at'];
    }

    public function getCreatedBy()
    {
        return $this->attributes['created_by'];
    }

    public function setCreatedBy($created_by)
    {
        $this->attributes['created_by'] = $created_by;
    }

    public function getUpdatedAt()
    {
        return $this->attributes['updated_at'];
    }

    public function getUpdatedBy()
    {
        return $this->attributes['updated_by'];
    }

    public function setUpdatedBy($updated_by)
    {
        $this->attributes['updated_by'] = $updated_by;
    }
}
